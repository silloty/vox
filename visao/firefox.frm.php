	<h4>O navegador Mozilla Firefox é um requisito do VOX </h4>
    
    <h1>Por que a GTI recomenda o navegador Mozilla Firefox</h1>
       <p class="featureText">

      No dia 14 de abril deste ano o Governo Federal lan&ccedil;ou o novo Portal do Software Livre. Este portal apresenta casos de sucesso do uso do <a href="http://www.softwarelivre.gov.br/SwLivre/">software livre</a> no  Governo Federal e oferece ferramentas de intera&ccedil;&atilde;o com os usu&aacute;rios. Al&eacute;m de incentivar todos os org&atilde;os que o comp&otilde;e a utilizar largamente softwares livres. Os objetivos dessa iniciativa est&atilde;o muito al&eacute;m da divulga&ccedil;&atilde;o deste movimento. Trata-se principalmente de um posicionamento, de uma tomada de lado nessa grande quest&atilde;o que hoje &eacute; discutida em todo mundo. Tanto &eacute; verdade, que podemos observar esse movimento em todos os n&iacute;veis da estrutura do governo: </p>
       <li>O MEC recentemente adotou sistemas livres para realizar seus servi&ccedil;os (<a href="http://www.softwarelivre.gov.br/documentos/palestramec/view">veja aqui</a>); </li>
       <li>O ex&eacute;rcito brasileiro contratou recentemente v&aacute;rios servi&ccedil;os visando a utiliza&ccedil;&atilde;o de software livre (<a href="http://www.serpro.gov.br/noticias-antigas/noticias-2004/20041221_01">veja aqui</a>);</li>
    <li>O Governo Federal pretende privilegiar o uso de software livre no sistema de TV Digital Brasileiro (<a href="http://www.cultura.gov.br/foruns_de_cultura/cultura_digital/na_midia/index.php?p=27383&more=1&c=1&pb=1">veja aqui</a>);</li>
    <li>O presidente do Tribunal Superior Eleitoral (TSE), ministro Marco Aur&eacute;lio, anunciou que as urnas eletr&ocirc;nicas nas elei&ccedil;&otilde;es 2008 funcionar&atilde;o com Linux (<a href="http://idgnow.uol.com.br/computacao_corporativa/2007/12/14/idgnoticia.2007-12-14.7421326619/">veja aqui</a>);</li>
    <li>Org&atilde;os p&uacute;blicos do governo de grande credibilidade como o Banco do Brasil e a Caixa Econ&ocirc;mica Federal adotam softwares livres (<a href="http://www.agenciabrasil.gov.br/noticias/2007/04/12/materia.2007-04-12.0879831108/view">veja aqui</a>).</li>
    <p>O IFMG - Campus Bambu&iacute;, al&eacute;m do fato de ser uma institui&ccedil;&atilde;o federal, pode ver nesse movimento de apoio do governo, grandes oportunidades para o crescimento na ado&ccedil;&atilde;o de tecnologias e softwares livres em sua infra-estrutura. Assim, o Mozilla Firefox como software livre que &eacute;, &eacute; uma recomenda&ccedil;&atilde;o da GTI para o uso em nossa institui&ccedil;&atilde;o. Mas n&atilde;o pelo fato apenas de ter seu c&oacute;digo aberto. Ele traz diversas vantagens que, do ponto de vista t&eacute;cnico, vem a contribuir imensamente com o trabalho desempenhado por nossa equipe dentro da institui&ccedil;&atilde;o. Al&eacute;m de n&atilde;o ter custos com licenciamento, permite ser personalizado para nossos fins, n&atilde;o exige procedimentos burocr&aacute;ticos para c&oacute;pias e redistribui&ccedil;&otilde;es, pode ser instalado tanto em computadores com Windows, Linux ou Mac, e &eacute; certificado pelas normas internacionais ISO.</p>
    <p>O download do mesmo &eacute; f&aacute;cil e r&aacute;pido. Em geral, leva menos de 3 minutos para ser conclu&iacute;do, comece clicando no link abaixo para instalar o mesmo no seu computador:</p>
       <p><a href="http://br.mozdev.org/firefox/download.html" class="style4" rel="external">BAIXAR O MOZILLA FIREFOX</a></p>
    <p>Se voc&ecirc; deseja conhecer mais vantagens oferecidas por este navegador, leia abaixo o texto extra&iacute;do do site oficial da funda&ccedil;&atilde;o Mozilla e escrito por Ben Goodger - l&iacute;der do time que constro&iacute; o Navegador Mozilla Firefox.    </p>
    <h2>VANTAGENS DO MOZILLA FIREFOX</h2>
<p>
O Mozilla Firefox procura visualizar a navegação de uma maneira global
- melhorando o desempenho no mundo real e aumentando o valor de seu&nbsp; tempo
online.&nbsp; O que você está fazendo online?&nbsp; O
que podemos fazer para facilitar para você? A velocidade na
exibição de páginas é somente uma parte da
equação. As ferramentas do Firefox mudam a
maneira com que você navega, elas farão da
navegação mais intuitiva, mais conveniente e ao mesmo
tempo tão rápida quanto você gostaria. De fato,
nós acreditamos que você <strong>ao menos dobrará </strong>seu
rendimento na internet.&nbsp; As ferramentas do
Firefox trabalham com você, não contra você e
procuram ser tão invisíveis como possível.
Continue lendo para saber mais.
</p>

    <h3 id="tab-browsing">Navegação em abas</h3>
    <table class="featureIcon" align="right" width="210">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/shot-tabs-1b.png" alt="Background Tabs in Action" height="152" width="200"><br>

            <strong class="featureCaption">Abas em segundo plano (mostrada em vermelho) permitem que você
            abra páginas para ler depois sem precisar deixar a página atual</strong>
        </td>
      </tr>
    </tbody></table>
    <p>

O Firefox faz da navegação <em>mais fácil do que
antes</em> com a melhor implementação da navegação em abas.
Navegação em abas muda a maneira que você usa a
internet, permitindo que você abra os links através de <em>abas em segundo plano</em>
e as lê então quando você estiver
pronto. Nenhuma nova janela lhe confundindo, nenhuma necessidade de
menus, apenas mantenha pressionado <strong>Ctrl</strong> ao clicar em
um link, ou utilize o <strong>botão do meio de seu mouse</strong>
(se você tiver um). A navegação em abas é um
recurso central para o Firefox, você pode ajustar sua
página principal para ser um grupo de abas com sites de sua
escolha. Você pode abrir uma pasta inteira de favoritos nas abas.


    </p><table class="featureIcon" align="left" width="210">

        <tbody><tr>
          <td><img class="featureIconImage" src="imagens/firefox/shot-tabs-2.png" alt="Abrir em abas o conteúdo de uma pasta de favoritos" height="75" width="200"><br>
              <strong class="featureCaption">"Abrir tudo em abas" abre todos as páginas da pasta
              em abas</strong>
          </td>
        </tr>
      </tbody></table>
    
    <p>
      Abas tornam a navegação na internet mais rápida e eficiente. Você pode
      concentrar-se na leitura de cada página sem a confusão de ter de ficar mudando
      janelas. Enquanto você lê a página, os links abertos em segundo plano vão sendo
      carregados. Quando você quiser lê-los eles já estarão prontos.
    </p>

    <h3 id="popup-blocking">Bloqueio de popups</h3>


<p>O Firefox efetivamente
bloqueia as tão irritantes janelas popup não
requisitadas enquanto permanece exibindo aquelas que você
realmente quer ver. Experimente a web como ela deveria ser - calma,
conveniente e acessível. </p>



    <table class="featureIcon" id="Table1" align="right" width="110">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/shot-popup-1.png" alt="Popups foram barrados" height="48" width="106"><br>
            <strong class="featureCaption">Ícone indicando que popups foram barrados</strong>
        </td>
      </tr>
    </tbody></table>
    <p>
      Alguns sites usam corretamente os popups. Quando o Firefox bloqueia um popup,
      um ícone é mostrado na barra de status. Clique no ícone para ver quais popups
      o Firefox barrou ao carregar a página. Você pode liberar os popups para o site.
    </p>

    <h3 id="integrated-search">Faça pesquisas mais rapidamente</h3>
    <table class="featureIcon" id="Table3" align="right" width="110">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/shot-search-1.png" alt="Campo de pesuisa em ação" height="94" width="100"><br>
            <strong class="featureCaption">O campo de pesquisa do Firefox lembra das pesquisas anteriores</strong>
        </td>

      </tr>
    </tbody></table>
    <p>
      Grande parte do seu tempo usando um navegador é gasto procurando por coisas.
      É por isso que o Firefox tem métodos de busca melhores do que qualquer outro browser.
      <strong>Ctrl+K</strong> leva você ao campo de pesquisa. Digite pelo que você procura
      e pressione Enter para pesquisar pelo Google ou outro dos mecanismos de
      busca padrão do Firefox. Digitar um termo de pesquisa na barra de endereços
      é equivalente ao "Estou com sorte" do Google (isto é, o Firefox automaticamente abre o
      primeiro resultado que o Google fornece para aquela pesquisa).
    </p>
    <p>


    </p><h3 id="simple-privacy">Privacidade simplificada</h3>
    <p>
      Sua privacidade é importante para nós. O Firefox torna fácil a verificação
      das informações que o browser armazena enquanto

</p><table class="featureIcon" id="Table6" align="left" width="160">
        <tbody><tr>
          <td><img class="featureIconImage" src="imagens/firefox/privacy.gif" alt="Ícone do painel de privacidade" height="52" width="81"><br>
              <strong class="featureCaption">O Firefox tem controles sofisticados de privacidade</strong>
          </td>
        </tr>
      </tbody></table>
      você navega, tais como as informações fornecidas a formulários, cookies, downloads,
      páginas visitadas (histórico) e assimpor diante. Você pode rapidamente apagar
      quaisquer destes itens do painel <strong>Privacidade</strong> de
      <strong>Ferramentas, Opções</strong>. Se você estiver usando um computador público ou compartilhado,
      você pode limpar instantaneamente todas as informações com um só clique.

    

    <h3 id="no-annoyances">Removedor de chateações</h3>

    <p>
      Além de evitar janelas popup, o Firefox também acaba com outros aborrecimentos

</p><table class="featureIcon" id="Table2" align="left" width="110">
        <tbody><tr>
          <td><img class="featureIconImage" src="imagens/firefox/shot-webfeatures-1.gif" alt="Ícone do painel Facilidades" height="54" width="87"><br>
              <strong class="featureCaption">O Firefox põe um fim aos truques chatos das páginas da internet</strong>
          </td>
        </tr>
      </tbody></table>

      que irritam os internautas há anos, como efeitos na barra de status (por exemplo,
      animações ou efeitos que tentam evitar com que você veja qual o endereço real de um link).
      Nós projetamos o Firefox para que as <em>definições padrão</em>
      ofereçam os <em>mínimos aborrecimentos</em>. Controles para estas opções podem ser
      encontradas no painel <strong>Facilidades</strong> em <strong>Opções</strong>.

    
    <p>
      O Firefox vem com os mesmo controles sofisticados do Mozilla que permitem que
      você controle cookies, JavaScript e outras configurações para satisfazer as suas necessidades.


    </p>


    <h3 id="bookmarks">Melhores favoritos e histórico</h3>
    <table class="featureIcon" align="right" width="210">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/shot-bookmarks-1.png" alt="Janelas de favoritos" height="142" width="200"><br>
            <strong class="featureCaption">O gerenciador de favoritos do Firefox torna simples
            a organização de páginas</strong>
        </td>
      </tr>
    </tbody></table>

    <p>
      O Firefox torna simples organizar seus favoritos com um prático gerenciador
      de favoritos (menu <strong>Favoritos, Organizar...</strong>).
      Crie pastas para agrupar itens relacionados e adicione observações para lembrar
      a você próprio o motivo de ter criado um determinado favorito.  Ordene por qualquer
      propriedade incluindo nome, endereço, última visita, etc. O Firefox também
      permite associar uma palavra-chave a qualquer favorito para que você o abra
      simplesmente digitando a palavra-chave na barra de endereços do browser.


    </p>
    <p>
      Tanto os favoritos quanto o histórico estão disponíveis no painel para acesso
      rápido. Apenas digite <strong>Ctrl+B</strong> para abrir o painel de favoritos
      <strong>Ctrl+H</strong> para o painel de histórico. Abra links com um clique.

    </p>

    <h3 id="accessibility">Melhor acessibilidade</h3>
    <p>
      O Firefox oferece o melhor conjunto de atalhos de teclado. <strong>Ctrl+L</strong>
      põe o foco na barra de endereços. <strong>Ctrl+K</strong> no campo de pesquisa.
      <strong>Ctrl+T</strong> abre uma nova aba e <strong>Ctrl+S</strong> faz o que deve
      fazer - salva a página atual. Todos os atalhos de teclado estão
      <a href="http://br.mozdev.org/firefox/ajuda/keyboard.html">disponíveis aqui</a>.
    </p>

    <table class="featureIcon" align="left" width="110">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/shot-taf-1.png" alt="Localizar ao digitar em ação" height="74" width="100"><br>
            <strong class="featureCaption">Localizar ao digitar em ação</strong>
        </td>
      </tr>
    </tbody></table>
    <p>
      O Firefox também apresenta uma pesquisa 'Localizar ao digitar' que torna
      simples a seleção de links para usuários com necessidades especiais (tais como os que
      precisam de leitores de tela ou mesmo usuários de laptops com dispositivos apontadores desconfortáveis).
      Apenas comece a digitar o nome de um link que você quer selecionar e o Firefox irá mover
      o foco para ele.


    </p>

    <h3 id="intelligent">Inteligente</h3>
    <table class="featureIcon" align="right" width="160">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/autocomplete.png" alt="AutoCompletar em ação" height="93" width="150"><br>
            <strong class="featureCaption">
            O Firefox o auxilia a preencher formulários e lembra o que você já pesquisou
            </strong>
        </td>

      </tr>
    </tbody></table>

    <p>
      O Firefox lembra o que você já digitou em formulários. Para refazer pesquisas
      ou preencher coisas como seu nome ou endereço de um jeito simples.
      O Firefox também possui um prático gerenciador de senhas que preenche automaticamente
      campos de nome de usuário e senha em páginas que pedem a sua identificação.
    </p>

<h3 id="responsive">Mais responsivo</h3>
<p>O Firefox apresenta melhores respostas na navegação
cotidiana do que concorrentes como o Internet Explorer.&nbsp; As páginas são
exibidas na tela mais rapidamente sem os constantes travamentos irritantes vistos
nos concorrentes. O efeito é um browser <em>simplesmente</em> rápido.</p>


<table class="featureIcon" align="left" width="110">
      <tbody><tr>
        <td><img class="featureIconImage" src="imagens/firefox/customize.png" alt="Customizing Toolbars" height="144" width="200"><br>
