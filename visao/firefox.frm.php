    <h3 id="cust-toolbars">Barras de ferramentas personalizáveis</h3>
    <p>
      Configure suas barras de ferramentas do jeito que você quiser. Economize espaço tendo tudo
      em apenas uma barra. Tenha um conjunto de barras com todas as opções disponíveis
      ao alcance de suas mãos. As barras de ferramentas do Firefox são totalmente personalizáveis.
      Selecione <strong>Exibir, Barras de ferramentas, Personalizar...</strong>
      e simplesmente arraste os itens que você deseja. O Firefox ainda permite que você crie novas
      barras de ferramentas. Extensões freqüentemente também criam novos itens na janela de personalizar.
    </p>


    <h3 id="extensible">Mais extensível</h3>
    <p>
      A interface do Firefox pode ser extendida por programadores usando padrões como
      XML, CSS, JavaScript etc, e também com complexos addons em C++. Exemplos de extensões
      vão de utilitários relativamente simples a programas complexos como o
      JavaScript Debugger e Document Inspector. Uma lista completa das extensões existentes
      está disponível <a href="https://update.mozilla.org/extensions/?application=firefox">aqui</a>.
      Extensões são facilmente instaladas.
    </p>

    <h3 id="gecko">Apoiado em ombros de gigantes</h3>
    <p>
      O Firefox é criado em cima do Mozilla Gecko, um dos mais rápidos mecanismos de renderização disponíveis.
    </p>

    <h3 id="compact">Mais espaço para páginas</h3>
    <p>
      Nós damos muita importância ao espaço para páginas web no Firefox. É por isso
      que o nosso navegador tem mais espaço dedicado a páginas web que o Internet Explorer,
      Opera ou Mozilla. O Firefox fornece 2% a mais de espaço para a web do que Mozilla, 4%
      a mais que o Internet Explorer e incríveis 10% a mais que o Opera (testado com
      configurações padrão, painéis fechados e janela maximizada em 1600x1200). Você
      pode conseguir ainda mais espaço reorganizando as barras de ferramentas. O Firefox
      também oferece o modo Tela inteira (pressione <strong>F11</strong> para entrar/sair) que
      dá ainda mais espaço para páginas web.
    </p>

    <h3 id="simple-ui">Interface simplificada</h3>
    <p>
      Nós investimos muito tempo fazendo o uso do Firefox mais fácil e óbvio.  Browsers
      como o Mozilla e Opera parecem mais focados em recursos obscuros, ao
      custo da facilidade de uso. É por isso que nós criamos um browser que funciona do
      jeito <strong>mais simples</strong> e <strong>mais direto</strong> assim que é
      instalado. As características padrão foram cuidadosamente escolhidas para que você
      não <em>precise</em> gastar seu tempo configurando o browser. Menus e janelas foram
      mantidas simples e sem as opções que apenas um pequeno número de pessoas usa.
      Se há algum recurso que o Mozilla ou Opera possui e está ausente, provavelmente é por um bom motivo.
      Mas se você precisa de algum aprimoramento em particular, há um grande número de excelentes
      <a href="https://update.mozilla.org/extensions/?application=firefox">extensões</a> que
      são livres para o uso.
    </p>

    <h3 id="themable">Personalize com temas</h3>

    <p>
      O Firefox suporta um grande número de
      <a href="https://update.mozilla.org/themes/?application=firefox">ótimos temas</a> que podem
      ser utilizados gratuitamente. Um tema pode alterar a aparência de todos os elementos,
      da janela principal a botões, menus, etc.
    </p>

    <h3 id="standards-support">Suporte aos principais padrões</h3>

    <p>
      O Firefox tem disparado o melhor suporte aos padrões W3C como CSS, DOM, etc.
      Onde browsers como o Opera podem falhar em uma página dinâmica, o Firefox geralmente tem sucesso.
    </p>

    <h3 id="fast">Rápido para baixar, rápido para usar</h3>
    <p>
      O Firefox pode ser seu em menos de 3 minutos.
      O Firefox inicia e funciona muito mais rápido que o Mozilla devido a significativa otimização de código.    </p>

